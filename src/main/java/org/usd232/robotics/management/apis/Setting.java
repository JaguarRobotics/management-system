package org.usd232.robotics.management.apis;

/**
 * The model class representing a setting
 * 
 * @author Zach Deibert
 * @since 1.0
 * @version 1.0
 */
public class Setting
{
    /**
     * The name of the setting
     * 
     * @since 1.0
     */
    public String      name;
    /**
     * The type of the setting
     * 
     * @since 1.0
     */
    public SettingType type;
    /**
     * The value of the setting
     * 
     * @since 1.0
     */
    public Object      value;
}
